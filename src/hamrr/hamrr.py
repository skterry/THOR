def _auto_imagecut_size(results, radius_arcsec: float) -> int:
    """
    Compute a cutout size (pixels, square) that spans the full search diameter.

    Pixel scales used:
      ACSWFC  → 50 mas/pix
      WFC3UV  → 40 mas/pix
    Falls back to ACSWFC if the instrument cannot be determined.
    """
    pixel_scale = 0.050  # arcsec/pix — ACS/WFC default
    if "HST_Field" in results.colnames and len(results) > 0:
        for field_val in results["HST_Field"]:
            s = str(field_val)
            if "WFC3UV" in s:
                pixel_scale = 0.040
                break
            if "ACSWFC" in s:
                pixel_scale = 0.050
                break
    return math.ceil(2 * radius_arcsec / pixel_scale)


def run_query(catalog, ra, dec, radius_arcsec, save, fmt, do_plot, do_lf, lf_band,
              lf_normalise=False, do_imagecut=False, imagecut_pid=17776, imagecut_size=None,
              output_dir="output"):
    """
    Execute a single cone search and handle output.
    Shared by both interactive mode and param-file mode.
    """
    import os
    os.makedirs(output_dir, exist_ok=True)

    radius = radius_arcsec / 3600.0

    try:
        results = catalog.cone_search(ra, dec, radius)
        catalog.print_summary(results, ra, dec, radius)
    except ValueError as e:
        print(f"\n  Error: {e}")
        return

    if len(results) == 0:
        return

    if save:
        out_name = os.path.join(output_dir,
                                f"hamrr_ra{ra:.3f}_dec{dec:.3f}"
                                f"_r{radius_arcsec:.2f}arcsec.{fmt}")
        results.write(out_name, overwrite=True)
        print(f"  Saved -> {out_name}")

    if do_plot:
        catalog.plot_cmd(results, ra, dec, radius_arcsec, output_dir=output_dir)

    if do_lf:
        catalog.plot_lf(results, ra, dec, radius_arcsec, band=lf_band,
                        normalise=lf_normalise, output_dir=output_dir)

    if do_imagecut:
        if imagecut_size is None:
            imagecut_size = _auto_imagecut_size(results, radius_arcsec)
            print(f"  imagecut_size auto-computed: {imagecut_size} px")
        get_mast_cutout(ra, dec, program_id=imagecut_pid, size=imagecut_size,
                        output_dir=output_dir)


