# ParamFileReader class — parses a params.in file
# =============================================================================

class ParamFileReader:
    """
    Reads and validates a plain-text parameter file (params.in).

    File format
    -----------
    Each non-blank, non-comment line is a  key = value  pair.
    Lines beginning with  #  are comments and are ignored.

    Supported keys
    --------------
    catalog_path   : Path to the .fits or .zip catalog file  (required)
    ra             : Search centre RA  — decimal degrees or HMS  (required)
    dec            : Search centre DEC — decimal degrees or DMS  (required)
    radius_arcsec  : Search radius in arcseconds                 (required)
    save_results   : yes / no   — save the result table          (default: no)
    save_format    : fits / csv / ecsv                           (default: fits)
    plot_cmd       : yes / no   — generate a CMD plot            (default: no)
    plot_lf        : yes / no   — generate a luminosity function (default: no)
    lf_band        : F814W / F606W  — band for the LF plot       (default: F814W)
    lf_normalise   : yes / no   — normalise LF by sky area       (default: no)
                     yes = stars mag⁻¹ arcmin⁻² ;  no = raw star counts
    imagecut         : yes / no   — download HST cutout from MAST   (default: no)
    imagecut_pid     : HST program ID to query                      (default: 17776)
    imagecut_size    : Cutout size in pixels (square)               (default: auto)
                     If omitted or set to 'auto', the size is computed from
                     radius_arcsec and the instrument pixel scale inferred from
                     the HST_Field column of the cone-search results:
                       ACSWFC  → 50 mas/pix   size = ceil(2 * radius_arcsec / 0.050)
                       WFC3UV  → 40 mas/pix   size = ceil(2 * radius_arcsec / 0.040)
    output_dir     : Directory for all output files               (default: output)

    Example params.in
    -----------------
    # HST Bulge Survey cone search parameters
    catalog_path  = terry_hst_bulge_survey_early_fields_cat.zip
    ra            = 17:43:37.2
    dec           = -29:00:25
    radius_arcsec = 18.0
    save_results  = yes
    save_format   = csv
    plot_cmd      = yes
    plot_lf       = yes
    lf_band       = F814W
    lf_normalise  = no
    """

    # Keys that must be present
    REQUIRED = {"catalog_path", "ra", "dec", "radius_arcsec"}

    # Keys with default values when absent
    DEFAULTS = {
        "save_results":  "no",
        "save_format":   "fits",
        "plot_cmd":      "no",
        "plot_lf":       "no",
        "lf_band":       "F814W",
        "lf_normalise":  "no",
        "imagecut":        "no",
        "imagecut_pid":    "17776",
        "imagecut_size":   "auto",
        "output_dir":    "output",
    }

    def __init__(self, filepath: str = "params.in"):
        self.filepath = filepath
        self._params  = dict(self.DEFAULTS)   # start with defaults
        self._parse()

    # ------------------------------------------------------------------
    # Parsing
    # ------------------------------------------------------------------

    def _parse(self) -> None:
        """Read the file and populate self._params."""
        with open(self.filepath, "r", encoding="utf-8") as f:
            for lineno, raw in enumerate(f, start=1):
                line = raw.strip()
                if not line or line.startswith("#"):
                    continue                   # skip blank lines and comments
                if "=" not in line:
                    raise ValueError(
                        f"params.in line {lineno}: expected 'key = value', got: {raw.rstrip()!r}"
                    )
                key, _, value = line.partition("=")
                key   = key.strip().lower()
                value = value.strip()
                if not key:
                    raise ValueError(f"params.in line {lineno}: empty key.")
                self._params[key] = value

        # Check all required keys are present
        missing = self.REQUIRED - self._params.keys()
        if missing:
            raise ValueError(
                f"params.in is missing required key(s): {', '.join(sorted(missing))}"
            )

    # ------------------------------------------------------------------
    # Typed accessors
    # ------------------------------------------------------------------

    @property
    def catalog_path(self) -> str:
        return self._params["catalog_path"]

    @property
    def ra(self) -> float:
        return CoordinateParser.parse_ra(self._params["ra"])

    @property
    def dec(self) -> float:
        return CoordinateParser.parse_dec(self._params["dec"])

    @property
    def radius_arcsec(self) -> float:
        v = float(self._params["radius_arcsec"])
        if v <= 0:
            raise ValueError(f"radius_arcsec must be > 0. Got: {v}")
        return v

    @property
    def save_results(self) -> bool:
        return self._params["save_results"].lower() in ("yes", "true", "1")

    @property
    def save_format(self) -> str:
        fmt = self._params["save_format"].lower()
        if fmt not in ("fits", "csv", "ecsv"):
            raise ValueError(f"save_format must be fits / csv / ecsv. Got: {fmt!r}")
        return fmt

    @property
    def plot_cmd(self) -> bool:
        return self._params["plot_cmd"].lower() in ("yes", "true", "1")

    @property
    def plot_lf(self) -> bool:
        return self._params["plot_lf"].lower() in ("yes", "true", "1")

    @property
    def lf_band(self) -> str:
        band = self._params["lf_band"].upper()
        if band not in ("F814W", "F606W"):
            raise ValueError(f"lf_band must be F814W or F606W. Got: {band!r}")
        return band

    @property
    def lf_normalise(self) -> bool:
        return self._params["lf_normalise"].lower() in ("yes", "true", "1")

    @property
    def imagecut(self) -> bool:
        return self._params["imagecut"].lower() in ("yes", "true", "1")

    @property
    def imagecut_pid(self) -> int:
        return int(self._params["imagecut_pid"])

    @property
    def imagecut_size(self):
        """Return the cutout size in pixels, or None to trigger auto-computation."""
        raw = self._params["imagecut_size"].strip().lower()
        if raw == "auto":
            return None
        v = int(raw)
        if v <= 0:
            raise ValueError(f"imagecut_size must be > 0. Got: {v}")
        return v

    @property
    def output_dir(self) -> str:
        return self._params["output_dir"]

    # ------------------------------------------------------------------
    # Summary
    # ------------------------------------------------------------------

    def print_summary(self) -> None:
        """Print the parsed parameters for confirmation."""
        print("  Parameters loaded from params.in:")
        for k, v in self._params.items():
            print(f"    {k:<20} = {v}")
        print()


