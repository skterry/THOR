class StarCatalog:
    """
    Loads an HST bulge survey FITS catalog and supports cone search queries.

    Attributes:
        filepath : Path to the FITS file.
        _table   : The raw Astropy Table loaded from the file.
    """

    def __init__(self, filepath: str):
        """
        Initialize and load the catalog from a FITS file.

        Args:
            filepath : Path to the .fits catalog file, or a .zip archive containing one.
        """
        self.filepath = filepath
        self._table   = None
        self._load()

    # ------------------------------------------------------------------
    # Private: loading
    # ------------------------------------------------------------------

    def _load(self) -> None:
        """
        Read the catalog into an Astropy Table.
        Accepts either a plain .fits file or a .zip archive containing one.
        The zip is decompressed directly into memory - nothing is written to disk.
        """
        #print(f"\nLoading catalog: {self.filepath} ...")

        if self.filepath.lower().endswith(".zip"):
            import zipfile
            with zipfile.ZipFile(self.filepath) as zf:
                fits_entries = [n for n in zf.namelist()
                                if n.lower().endswith(".fits")]
                if not fits_entries:
                    raise ValueError(
                        f"No .fits file found inside '{self.filepath}'."
                    )
                fits_name = fits_entries[0]
                #print(f"  Reading '{fits_name}' from zip ...")
                with zf.open(fits_name) as f:
                    self._table = Table.read(f)
        else:
            self._table = Table.read(self.filepath)

        #print(f"  {self.star_count:,} sources loaded.")
        #print(f"  Columns: {self._table.colnames}\n")

    # ------------------------------------------------------------------
    # Properties
    # ------------------------------------------------------------------

    @property
    def star_count(self) -> int:
        """Total number of sources in the catalog (read-only)."""
        return len(self._table)

    # ------------------------------------------------------------------
    # Core: cone search
    # ------------------------------------------------------------------

    def cone_search(self, ra: float, dec: float, radius: float) -> Table:
        """
        Return all catalog sources within a cone on the sky.

        The returned table contains every original catalog column, plus a
        new 'distance' column (degrees) giving each source's angular
        separation from (ra, dec). The table is sorted nearest-first.

        Args:
            ra     : Right Ascension of the search center (decimal degrees, 0–360)
            dec    : Declination of the search center (decimal degrees, -90 to +90)
            radius : Search radius in decimal degrees (must be > 0)

        Returns:
            An astropy.table.Table of matching sources, sorted by 'distance'.

        Raises:
            ValueError : If ra, dec, or radius are outside valid ranges.
        """
        # --- Input validation ---
        if not (0 <= ra < 360):
            raise ValueError(f"RA must be in [0, 360). Got: {ra}")
        if not (-90 <= dec <= 90):
            raise ValueError(f"DEC must be in [-90, 90]. Got: {dec}")
        if radius <= 0:
            raise ValueError(f"Search radius must be > 0. Got: {radius}")

        # --- Vectorised Haversine over the whole catalog ---
        # Working in radians on numpy arrays is much faster than a Python loop
        ra1  = np.radians(self._table["RA"].data.astype(float))
        dec1 = np.radians(self._table["DEC"].data.astype(float))
        ra2  = math.radians(ra)
        dec2 = math.radians(dec)

        d_ra  = ra2  - ra1
        d_dec = dec2 - dec1

        a = (np.sin(d_dec / 2) ** 2
             + np.cos(dec1) * math.cos(dec2) * np.sin(d_ra / 2) ** 2)

        separations = np.degrees(2 * np.arcsin(np.sqrt(a)))   # degrees

        # --- Filter to sources inside the cone ---
        inside = separations <= radius
        result = self._table[inside].copy()

        # --- Add the distance column (arcseconds) ---
        dist_col = Column(
            data        = separations[inside] * 3600.0,
            name        = "distance",
            unit        = u.arcsec,
            description = "Angular separation from search center (arcseconds)",
        )
        result.add_column(dist_col)

        # --- Sort nearest-first ---
        result.sort("distance")

        return result

    # ------------------------------------------------------------------
    # Display helpers
    # ------------------------------------------------------------------

    def print_summary(self, result: Table, ra: float, dec: float,
                      radius: float) -> None:
        """Print a summary header and a short preview of the result table."""
        print()
        print("=" * 70)
        print("  Search Results")
        print(f"  Center  : {CoordinateParser.format_summary(ra, dec)}")
        print(f"  Radius  : {radius * 3600:.4f} arcsec")
        print(f"  Matches : {len(result)}")
        print("=" * 70)

        if len(result) == 0:
            print("  No sources found in this region.\n")
            return

        # Show a readable preview (first 20 rows, key columns)
        preview_cols = ["HST_ID", "RA", "DEC", "F606W_Vegamag",
                        "F814W_Vegamag", "HST_Field", "distance"]
        available = [c for c in preview_cols if c in result.colnames]
        print(result[available][:20])

        if len(result) > 20:
            print(f"  ... and {len(result) - 20} more rows.")

        print()

    def plot_cmd(self, result: Table, ra: float, dec: float,
                 radius_arcsec: float, output_dir: str = "output") -> None:
        """
        Plot a colour-magnitude diagram (CMD) for the cone search results
        and save it as a PNG file.

        X-axis : F606W − F814W
        Y-axis : F814W

        Stars missing either magnitude are silently excluded from the plot.

        Args:
            result        : Astropy Table returned by cone_search().
            ra            : Search centre RA (decimal degrees)  — used in filename/title.
            dec           : Search centre DEC (decimal degrees) — used in filename/title.
            radius_arcsec : Search radius in arcseconds         — used in filename/title.
            output_dir    : Directory in which to save the PNG  (default: "output").
        """
        if len(result) == 0:
            print("  No stars to plot.")
            return

        # --- Extract magnitudes, masking missing values ---
        f606w = np.array(result["F606W_Vegamag"].data.astype(float))
        f814w = np.array(result["F814W_Vegamag"].data.astype(float))

        valid = np.isfinite(f606w) & np.isfinite(f814w)
        n_valid = valid.sum()

        if n_valid == 0:
            print("  No stars with valid F606W and F814W magnitudes — skipping CMD plot.")
            return

        color = f606w[valid] - f814w[valid]   # F606W − F814W
        mag   = f814w[valid]                  # F814W

        # --- Plot ---
        fig, ax = plt.subplots(figsize=(6, 8))

        ax.scatter(
            color, mag,
            s       = 8,          # marker size
            alpha   = 0.4,
            color   = "steelblue",
            edgecolors = "none",
        )

        # Invert Y-axis: brighter (lower magnitude number) at the top
        ax.invert_yaxis()

        # Labels and title
        ax.set_xlabel("F606W − F814W", fontsize=12)
        ax.set_ylabel("F814W [mag]", fontsize=12)
        ax.set_title(
            f"Color-Magnitude Diagram\n"
            f"RA={ra:.3f}°  DEC={dec:+.3f}°  r={radius_arcsec:.2f}\"  "
            f"({n_valid} stars)",
            fontsize=10,
        )

        #ax.grid(True, linestyle="--", alpha=0.3)
        fig.tight_layout()

        # --- Save ---
        import os
        os.makedirs(output_dir, exist_ok=True)
        out_name = os.path.join(output_dir,
                                f"hamrr_cmd_ra{ra:.3f}_dec{dec:.3f}"
                                f"_r{radius_arcsec:.2f}arcsec.png")
        fig.savefig(out_name, dpi=150)
        plt.close(fig)
        print(f"  CMD saved → {out_name}")


    def plot_lf(self, result: Table, ra: float, dec: float,
                radius_arcsec: float, band: str = "F814W",
                normalise: bool = False, output_dir: str = "output") -> None:
        """
        Plot a luminosity function (magnitude histogram) for the cone search
        results and save it as a PNG file.

        The luminosity function shows star counts (Y-axis) per magnitude bin
        (X-axis) for a chosen filter band. Bin width is fixed at 0.3 magnitudes.

        Stars with missing/non-finite magnitudes are silently excluded.

        Args:
            result        : Astropy Table returned by cone_search().
            ra            : Search centre RA (decimal degrees)  — used in filename/title.
            dec           : Search centre DEC (decimal degrees) — used in filename/title.
            radius_arcsec : Search radius in arcseconds         — used in filename/title.
            band          : Filter band to plot — "F814W" (default) or "F606W".
            normalise     : If True, divide counts by the cone area in arcmin²,
                            giving units of stars mag⁻¹ arcmin⁻².
                            If False (default), plot raw star counts.
            output_dir    : Directory in which to save the PNG  (default: "output").
        """
        if len(result) == 0:
            print("  No stars to plot.")
            return

        # Map band name to catalog column
        col_map = {
            "F814W": "F814W_Vegamag",
            "F606W": "F606W_Vegamag",
        }
        band = band.upper()
        if band not in col_map:
            print(f"  Unknown band '{band}' — choose F814W or F606W.")
            return

        col_name = col_map[band]
        if col_name not in result.colnames:
            print(f"  Column '{col_name}' not found in results — skipping LF plot.")
            return

        # --- Extract magnitudes, dropping missing values ---
        mags = np.array(result[col_name].data.astype(float))
        mags = mags[np.isfinite(mags)]
        n_valid = len(mags)

        if n_valid == 0:
            print(f"  No stars with valid {band} magnitudes — skipping LF plot.")
            return

        # --- Cone area (always computed; only used when normalise=True) ---
        radius_arcmin = radius_arcsec / 60.0
        area_arcmin2  = math.pi * radius_arcmin ** 2

        # --- Build histogram bins aligned to 0.3 mag intervals ---
        bin_width = 0.3
        mag_min   = np.floor(mags.min() / bin_width) * bin_width
        mag_max   = np.ceil( mags.max() / bin_width) * bin_width
        bins      = np.arange(mag_min, mag_max + bin_width, bin_width)

        counts, _   = np.histogram(mags, bins=bins)
        bin_centers = 0.5 * (bins[:-1] + bins[1:])

        # Normalise by sky area if requested, otherwise use raw counts
        if normalise:
            y_values = counts / area_arcmin2
            y_label  = r"stars / mag / arcmin$^2$"
            area_str = f"  area={area_arcmin2:.3f} arcmin²"
        else:
            y_values = counts.astype(float)
            y_label  = "Number of stars"
            area_str = ""

        # --- Plot ---
        fig, ax = plt.subplots(figsize=(7, 5))

        ax.step(
            bin_centers,
            y_values,
            color     = "royalblue",
            linewidth = 0.9,
            alpha     = 0.99,
        )

        ax.set_yscale('log')
        ax.set_xlabel(f"{band} [mag]", fontsize=12)
        ax.set_ylabel(y_label, fontsize=12)
        ax.set_title(
            f"Luminosity Function ({band})\n"
            f"RA={ra:.3f}°  DEC={dec:+.3f}°  r={radius_arcsec:.2f}\""
            f"{area_str}  ({n_valid} stars,  bin width={bin_width} mag)",
            fontsize=8,
        )

        #ax.grid(True, linestyle="--", alpha=0.3, axis="y")
        fig.tight_layout()

        # --- Save ---
        import os
        os.makedirs(output_dir, exist_ok=True)
        out_name = os.path.join(output_dir,
                                f"hamrr_lf_{band.lower()}_ra{ra:.3f}_dec{dec:.3f}"
                                f"_r{radius_arcsec:.2f}arcsec.png")
        fig.savefig(out_name, dpi=150)
        plt.close(fig)
        print(f"  LF saved → {out_name}")

    # ------------------------------------------------------------------
    # Dunder methods
    # ------------------------------------------------------------------

    def __repr__(self) -> str:
        return f"StarCatalog(filepath={self.filepath!r}, stars={self.star_count:,})"

    def __str__(self) -> str:
        return f"HST Bulge Survey Catalog — {self.star_count:,} sources  [{self.filepath}]"


