# Main
# =============================================================================
 
def main():
    print()
    print("╔══════════════════════════════════════════════════╗")
    print("║    HST Bulge Survey — Cone Search Query Tool     ║")
    print("╚══════════════════════════════════════════════════╝")
 
    # --- Load catalog ---
    fits_path = input("\nPath to FITS catalog file: ").strip()
    try:
        catalog = StarCatalog(fits_path)
    except FileNotFoundError:
        print(f"  Error: file not found — {fits_path}")
        return
    except Exception as e:
        print(f"  Error loading catalog: {e}")
        return
 
    print(catalog)
 
    # --- Interactive query loop ---
    while True:
        print("\nEnter search parameters (Ctrl-C to quit):")
        print("  Coordinates may be decimal degrees  e.g.  266.405  /  -29.007")
        print("  or equatorial (HMS / DMS)            e.g.  17h43m37s  /  -29d00m25s")
        print("  or colon-separated                   e.g.  17:43:37   /  -29:00:25")
        print()
 
        # RA
        while True:
            try:
                ra = CoordinateParser.parse_ra(input("  RA  : ").strip())
                break
            except ValueError as e:
                print(f"  Error: {e}")
 
        # DEC
        while True:
            try:
                dec = CoordinateParser.parse_dec(input("  DEC : ").strip())
                break
            except ValueError as e:
                print(f"  Error: {e}")
 
        # Confirm what was parsed
        print(f"\n  Parsed → {CoordinateParser.format_summary(ra, dec)}")
 
        radius_arcsec = get_float_input("  Radius (arcseconds, > 0) : ", 1e-9)
        radius = radius_arcsec / 3600.0   # convert to degrees for cone_search()
 
        try:
            results = catalog.cone_search(ra, dec, radius)
            catalog.print_summary(results, ra, dec, radius)
        except ValueError as e:
            print(f"\n  Error: {e}")
            continue
 
        # --- Optional: save to file ---
        if len(results) > 0:
            save = input("  Save results to a file? (y/n): ").strip().lower()
            if save == "y":
                fmt = input("  Format — fits / csv / ecsv [default: fits]: ").strip().lower()
                fmt = fmt if fmt in ("fits", "csv", "ecsv") else "fits"
                out_name = f"cone_search_ra{ra:.6f}_dec{dec:.6f}_r{radius_arcsec:.4f}arcsec.{fmt}"
                results.write(out_name, overwrite=True)
                print(f"  Saved → {out_name}")
 
            plot = input("  Plot colour-magnitude diagram? (y/n): ").strip().lower()
            if plot == "y":
                catalog.plot_cmd(results, ra, dec, radius_arcsec)
 
        again = input("\nRun another search? (y/n): ").strip().lower()
        if again != "y":
            break
 
    print("\nGoodbye!\n")
 
 
if __name__ == "__main__":
    main()
